-- 4. Table `school_info` (for rich text/HTML contents of sections)
CREATE TABLE IF NOT EXISTS `school_info` (
  `id` INT AUTO_INCREMENT PRIMARY KEY,
  `section_key` VARCHAR(50) NOT NULL UNIQUE,
  `content_th` LONGTEXT NOT NULL,
  `content_en` LONGTEXT NOT NULL,
  `updated_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- 5. Table `school_executives` (for executive list directory)
CREATE TABLE IF NOT EXISTS `school_executives` (
  `id` INT AUTO_INCREMENT PRIMARY KEY,
  `name_th` VARCHAR(100) NOT NULL,
  `name_en` VARCHAR(100) NOT NULL,
  `position_th` VARCHAR(100) NOT NULL,
  `position_en` VARCHAR(100) NOT NULL,
  `period` VARCHAR(50) NOT NULL,
  `image_path` VARCHAR(255) DEFAULT NULL,
  `is_current` TINYINT(1) NOT NULL DEFAULT 0,
  `order_num` INT NOT NULL DEFAULT 0
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

